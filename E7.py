Dia = int(input('Digite um dia da semana (numeral): \n'))

if Dia == 1:
    print('Domingo')
elif Dia == 2:
    print('Segunda-Feira')
elif Dia == 3:
    print('Terça-Feira')
elif Dia == 4:
    print('Quarta-Feira')
elif Dia == 5:
    print('Quinta-Feira')
elif Dia == 6:
    print('Sexta-Feira')
elif Dia == 7:
    print('Sábado')
else:
    print('Valor inválido.')